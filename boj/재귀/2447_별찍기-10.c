#include <stdio.h>
char star[6561][6561];
void shape(int x,int y,int n){
    if(n==3){
        star[x][y]='*';
        star[x][y+1]='*';
        star[x][y+2]='*';
        
        star[x+1][y]='*';
        star[x+1][y+2]='*';
        
        star[x+2][y]='*';
        star[x+2][y+1]='*';
        star[x+2][y+2]='*';
        
        return;
    }
    shape(x,y,n/3);
    shape(x,y+n/3,n/3);
    shape(x,y+2*n/3,n/3);
    
    shape(x+n/3,y,n/3);
    shape(x+n/3,y+2*n/3,n/3);
    
    shape(x+2*n/3,y,n/3);
    shape(x+2*n/3,y+n/3,n/3);
    shape(x+2*n/3,y+2*n/3,n/3);
    
}
int main() {
    int n;
    scanf("%d",&n);
    shape(0,0,n);
    
    for(int i=0;i<n;i++){
        for(int j=0;j<n;j++){
            if(star[i][j]=='*')
                printf("*");
            else
                printf(" ");
        }
        printf("\n");
    }   
    return 0;
}